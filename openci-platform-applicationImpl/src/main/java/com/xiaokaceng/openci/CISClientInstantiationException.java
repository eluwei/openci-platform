package com.xiaokaceng.openci;

public class CISClientInstantiationException extends RuntimeException {

	private static final long serialVersionUID = -1808922111077607002L;

	public CISClientInstantiationException() {
		super();
	}

	public CISClientInstantiationException(String message, Throwable cause) {
		super(message, cause);
	}

	public CISClientInstantiationException(String message) {
		super(message);
	}

	public CISClientInstantiationException(Throwable cause) {
		super(cause);
	}

}
