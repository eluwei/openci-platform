package com.xiaokaceng.openci;

public class DefaultEmailNotSettingsException extends RuntimeException {

	private static final long serialVersionUID = 2723975111900098706L;

	public DefaultEmailNotSettingsException() {
		super();
	}

	public DefaultEmailNotSettingsException(String message, Throwable cause) {
		super(message, cause);
	}

	public DefaultEmailNotSettingsException(String message) {
		super(message);
	}

	public DefaultEmailNotSettingsException(Throwable cause) {
		super(cause);
	}
	
}
